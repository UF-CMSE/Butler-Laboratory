      <p><img src="images/spotlight/jojo.jpg" alt="Jojo Photo" align="left" style="padding:10px">My childhood revolved around science; ever since I was young, I would hear stories from some family members about their adventures conducting scientific research. Therefore it was not surprising that I came to love the subject at a young age. However, it was not until I was 12 years old that I discovered the love of a particular branch of science: chemistry. Reading in the library about Jabir Ibn Hayyan, the Arab chemist who is credited with establishing some well-known procedures; e.g., distillation and recrystallization, stimulated my scientific enthusiasm. From that point onward, I studied hard to become an excellent student so I could follow my dream and become a successful chemist. Pursuing my dream also motivated me to study the English language to acquire more knowledge of science. I am here today because I could never see myself doing anything else but chemistry.</p>
	<p>I joined the chemistry department at Princess Nora University and received my bachelor degree in 2002. In 2007, I got a three-year English translation diploma from the college of languages and translation at King Saud University. After that I got the chance to start a master’s degree program in organic chemistry, at Princess Nora University, under the supervision of Prof. Fares Alsewailem. My main master research focused on minimizing the energy required to recycle plastic wastes by measuring the activation energy needed for the pyrolysis process of virgin and discarded plastic wastes.  This is when I developed my interest in polymer chemistry and was committed to become the first female polymer chemist in the departments and faculties of Saudi Arabia. My goal was to be a pioneer in this field for female polymer chemists and make a change. </p> 
	<p>In 2011, I joined the chemistry department at Princess Nora University as a lecturer. A year later, I was granted a scholarship to further expand my knowledge of polymers at any university of my choice. The great research conducted at the American schools and the number of publications issued on the subject every year aided my decision to go to the United States of America. In the same year, I was fortuitous to attend a talk by Prof. Kenneth Wagner at the petrochemical conference in Riyadh. The passion he showed talking about the Butler Polymer Research Laboratories at University of Florida inspired me to join the polymer program at the University of Florida. </p>
<p>To foster my knowledge in polymer chemistry, I joined the doctoral program in chemistry, in 2012, at the University of Florida under the guidance of Prof. Brent Sumerlin. My research projects focus mainly on developing novel stimuli-responsive polymeric nanocarriers (such as micelles and hyperbranched polymers) for anti-cancer drug delivery and release based on poly (N-(2-hydroxypropyl) methacrylamide). I utilized controlled radical polymerization techniques, especially reversible addition-fragmentation chain transfer (RAFT), to synthesize polymers with controlled molecular weights, predetermined architectures, and low polydispersities. With the guidance of Prof. Sumerlin, I will continue developing and expanding my expertise of polymer chemistry especially with the great foundation I gained by joining the Butler Polymer Research Laboratories and the Sumerlin Group. 
